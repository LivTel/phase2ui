    public ISequenceComponent getSequenceComponent() {

        String name = (String) jcbBranchType.getSelectedItem();

        XBranchComponent branchComponent = new XBranchComponent(name);

        branchComponent.addChildComponent(new XIteratorComponent(CONST.FRODO_RED, new XIteratorRepeatCountCondition(1)));
        branchComponent.addChildComponent(new XIteratorComponent(CONST.FRODO_BLUE, new XIteratorRepeatCountCondition(1)));

        return branchComponent;
    }
    
